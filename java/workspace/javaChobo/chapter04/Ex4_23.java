package chapter04;

public class Ex4_23 {

	public static void main(String[] args) {
		int i = 5;
		
		while (i--!=0) {
			System.out.println(i+ " - I can do it.");
		}
		 System.out.println("================");
		int j = 5;
		while (--j!=0) {
			System.out.println(j+ " - I can do it.");
	 }	
	}
}
